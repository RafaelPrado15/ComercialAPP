// Menu Lateral


// Abre o modal ao clicar no menu button
function toggleMenuLateral() {
    var menuLateral = document.getElementById('menuLateral');
    menuLateral.classList.toggle('ativo');
  }
  
  //Oculta a pagina ao clicar fora
  document.addEventListener('click', function(event) {
    var menuLateral = document.getElementById('menuLateral');
    var iconeLista = document.getElementById('iconeLista');
    var closeBtn = document.getElementById('closeBtn');
  
    // Verifica se o clique foi fora do menu lateral, do ícone de lista e do botão de fechar
    if (!menuLateral.contains(event.target) && event.target !== iconeLista && event.target !== closeBtn) {
        menuLateral.classList.remove('ativo');
    }
  });
  
  //oculta menu linguagens
  document.addEventListener('click', function(event) {
    var languageOptions = document.getElementById('languageOptions');
    var buttonL = document.querySelector('.buttonL');
  
    // Verifica se o clique foi fora do modal de linguagens e do botão de ativação do modal
    if (!languageOptions.contains(event.target) && event.target !== buttonL) {
        languageOptions.style.display = 'none';
    }
  });
  

